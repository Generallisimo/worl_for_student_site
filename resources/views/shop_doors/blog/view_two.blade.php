                                       <h2>What are composite doors made from? </h2>
                                       <p class="f-400" style="text-align: start;">Composite doors are made in three parts, an outer frame, subframe and solid timber core. This blend of materials, each with different properties combine to create a hardwearing, long lasting and most importantly good-looking door. So what are some of the reasons for you to make your next front door a composite front door?</p>
                                        <h2>5 Benefits of Composite Front Doors</h2>
                                        <h4 style="text-align: start;">1. Weather resistant</h4>
                                        <p class="f-400" style="text-align: start;">Standard Doors are always going to be affected by temperature, and climatic changes, this not only affects the physical appearance of your door but also affects the quality by creating weaknesses and issues with how the door opens and closes. One of the key benefits of GRP composite doors are how well they withstand the elements over time, making them an extremely durable choice and a very practical long term investment in your home. </br> These doors are also windproof, and will not rot, rust, warp in the heat or swell when it rains, making them the perfect choice for a front door or back door. It’s common to get around 35 years of use from a composite door, so it’s a great investment and a cost effective solution for your home too.</p>
                                        <h4 style="text-align: start;">2. Easy Maintenance</h4>
                                        <p class="f-400" style="text-align: start;">Standard doors require treatments to maintain upkeep such as stains, paint and varnish to protect them from the weather and the environment, which need to be reapplied regularly. However, with composite doors, their GRP skin ensures that the door is completely weather resistant and does not need to be coated with treatments to prevent rot, peeling or flaking. This is why so many homeowners have turned to composite doors as an alternative, as these doors require very little maintenance in order to keep them working as they should.</p>
                                        <h4 style="text-align: start;">3. Energy Efficient</h4>
                                        <p class="f-400" style="text-align: start;">With growing focus on energy conservation and efficiency, composite doors have also been designed with the energy conscious customer in mind. These doors are great at retaining heat and reducing heat loss. </br> Composite doors have also been proven to be approximately 10xs more energy efficient than standard doors, due to their insulated foam core, but also due to their structure as a whole which is great for energy conscious customers.</p>
                                        <h4 style="text-align: start;">4. Security you can trust</h4>
                                        <p class="f-400" style="text-align: start;">Composite doors are typically much sturdier than their standard counterparts, the frame is reinforced with galvanised steel, and the GRP skin is able to withstand a larger force than other doors. This, combined with the fact that composite doors are thicker than most other doors, makes composite doors a great choice for security. </br> Most GRP composite doors also come with multi point locking systems as standard, which ensures your home is more secure than a traditional door with a simple locking system.</p>
