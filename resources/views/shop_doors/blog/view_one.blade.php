                                        <h2>How to fit your new internal door</h2>
                                        <p class="f-400" style="text-align: start;">Step 1: First, check any instructions that may come with your new door to see if there are any special requirements. Next you’ll need to remove the old door from its frame. To do this simply knock out the hinge pin from your hinges and then lift the old door out of the frame. You may need the help of a second person for this. If the hinge pin is tight and hard to remove, gently knock it out using a hammer and a nail with a flattened tip. </p>    
                                        <p class="f-400" style="text-align: start;">Top tip: Make sure you have removed any nails that are sticking out of the frame and sand down any splinters or sharp edges.</p>    
                                        <p class="f-400" style="text-align: start;">Step 2: Stand the new door up in the doorway to check whether it needs trimming. If it does, use a pencil to mark the top of the door opening onto the new door.</p>    
