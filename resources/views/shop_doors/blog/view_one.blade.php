                                        <p class="f-400" style="text-align: start;">Step 4: After you have measured and trimmed the height of your new door, transfer the width measurement of your doorframe onto the door. Using a tape measure and pencil, mark up on the door and then use a planer to remove the excess from the width. Once the excess is removed, sand the edges of the door</p>
                                        <p class="f-400" style="text-align: start;">Step 5: Using a wedge, mark the hinges. Place the door on the doorframe and use the wedges to support it. On the door pencil mark the locations of the bottom, middle, and top hinges. Following the mark points you previously drew on the door, the next step is to mark the hinge positions on to the side of the door. Additionally, you need to specify the hinge's depth.</p>
                                        <p class="f-400" style="text-align: start;">Step 6: Chisel around the outline of the hinge – the tools you’ll need to do this is a drill, drill bits, chisel, hammer and screwdriver.</p>
                                        <p class="f-400" style="text-align: start;">Top Tip: To prevent splitting, cut at 5 mm intervals. To eliminate extra material use the chisel flat down.</p>
                                        <p class="f-400" style="text-align: start;">Mark the holes after grabbing the hinges. Once the holes have been marked, drill the pilot holes, and attach all of the hinges to the door. </p>
